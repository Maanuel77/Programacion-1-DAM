// Class to add methods for the different operations of a rectangle

public class RectangleArea {
	
	/**
	 * Calculate the area of a rectangle
	 * 
	 * @param base
	 * @param altura
	 * @return area
	 */

	   public double calcularArea(double base, double altura) {
	       return base * altura;
	   }

	}