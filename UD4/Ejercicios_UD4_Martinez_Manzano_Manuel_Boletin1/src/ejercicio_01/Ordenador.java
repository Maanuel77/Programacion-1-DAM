package ejercicio_01;

public class Ordenador {

	private double capacidad_Disco_Duro;
	private double frecuencia_CPU;
	private String marca;
	private double precio_Base;
	
	public Ordenador(double capacidad_Disco_Duro, double frecuencia_CPU, String marca, double precio_Base) {
		super();
		this.capacidad_Disco_Duro = capacidad_Disco_Duro;
		this.frecuencia_CPU = frecuencia_CPU;
		this.marca = marca;
		this.precio_Base = precio_Base;
	}
	
}
